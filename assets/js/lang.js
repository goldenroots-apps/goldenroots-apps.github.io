var LANG_FLAGS = {en:'us',th:'th',ja:'jp',zh:'cn',ko:'kr',hi:'in',es:'es'};

function setLang(lang) {
  var t = LANGS[lang];
  if (!t) return;
  document.documentElement.lang = lang;
  document.documentElement.dir = t.dir;
  localStorage.setItem('lang', lang);
  document.querySelectorAll('[data-i18n]').forEach(function(el) {
    var key = el.getAttribute('data-i18n');
    if (t[key]) el.innerHTML = t[key];
  });
  // Update custom dropdown button
  var flagEl = document.getElementById('lang-flag');
  var codeEl = document.getElementById('lang-code');
  if (flagEl) { flagEl.className = 'fi fi-' + (LANG_FLAGS[lang] || 'us'); }
  if (codeEl) codeEl.textContent = lang.toUpperCase();
  // Update active state in menu
  document.querySelectorAll('.lang-option').forEach(function(el) {
    el.classList.toggle('active', el.getAttribute('data-lang') === lang);
  });
}

function toggleLangMenu() {
  var dd = document.getElementById('lang-dropdown');
  if (dd) dd.classList.toggle('open');
}

function pickLang(lang) {
  setLang(lang);
  var dd = document.getElementById('lang-dropdown');
  if (dd) dd.classList.remove('open');
}

// Close dropdown when clicking outside
document.addEventListener('click', function(e) {
  var dd = document.getElementById('lang-dropdown');
  if (dd && !dd.contains(e.target)) dd.classList.remove('open');
});

(function() {
  var saved = localStorage.getItem('lang');
  var browser = navigator.language ? navigator.language.slice(0, 2) : 'en';
  var lang = saved || (LANGS[browser] ? browser : 'en');
  document.addEventListener('DOMContentLoaded', function() { setLang(lang); });
})();
